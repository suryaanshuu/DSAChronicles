class Solution {
  public:
    void reverseArray(vector<int> &arr) {
        stack<int> res;
        int n = arr.size();
        for(int i = 0; i < n; i++){
            res.push(arr[i]);
        }
        
        for(int i = 0; i < n; i++){
            arr[i] = res.top();
            res.pop();
        }
    }
};

